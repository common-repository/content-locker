<h2>Creating a Facebook App</h2>
<p>
    Creating social apps and connecting them to the Content Locker plugin is essential for the proper functioning of the Sign in Locker feature. Don't be scared, though, this process is very simple, and we will show you exactly how to do this.
</p>
<p>
    To make the Facebook Sign in Locker feature work, you need to create a Facebook app by following these steps:
</p>
<p>
    1. Go to <a href="https://developers.facebook.com/">developers.facebook.com</a> and log in, if you haven't already. Click on the "Create App" button as shown in the screenshot below:
</p>
<p class="mts-cl-img">
    <img src="<?php echo cl()->plugin_url() ?>/admin/views/how-to-use/img/15.jpg" />
</p>

<p>
    2. A new popup asking for your app details will open up. Enter the app's display name, your contact email and your app's category. Once you are done, click the "Create App ID" button as shown in the screenshot:
</p>
<p class="mts-cl-img">
    <img src="<?php echo cl()->plugin_url() ?>/admin/views/how-to-use/img/16.jpg" />
</p>
<p>
    3. Once your app ID is created, you want to go to the "Settings" section on the next screen:
</p>
<p class="mts-cl-img">
    <img src="<?php echo cl()->plugin_url() ?>/admin/views/how-to-use/img/17.jpg" />
</p>

<p>
    4. Add Your Website's URL, both with and without the starting 'www'. Then, select "website" as the platform and enter your website's URL.
</p>
<p>
    5. Head over to the "App Review" section and make the App public like shown in the screenshot:
</p>
<p class="mts-cl-img">
    <img src="<?php echo cl()->plugin_url() ?>/admin/views/how-to-use/img/18.jpg" />
</p>
<p>
    6. Once that is done, Head back to the Dashboard of the Facebook App and grab the App ID. Copy this App so you can paste it in the Content Locker plugin.
</p>
<p class="mts-cl-img">
    <img src="<?php echo cl()->plugin_url() ?>/admin/views/how-to-use/img/19.jpg" />
</p>

<p>
    7. Go back to the <a href="<?php echo cl_get_admin_url('settings#facebook_appid') ?>">Content Locker settings page</a> and paste the App ID you copied in the last step.
</p>
<p class="mts-cl-img">
    <img src="<?php echo cl()->plugin_url() ?>/admin/views/how-to-use/img/20.jpg" />
</p>
